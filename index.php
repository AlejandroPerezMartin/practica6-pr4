<p><a href="rutas/tabla.html" class="button" title="Ver resumen de las rutas">Resumen de las rutas</a></p>

<p>Esta guía quiere abrir una puerta por la que acceder a los numerosos caminos que surcan la Vega de San Mateo, Tejeda, Valsequillo y la Villa de Santa Brígida; rutas que unen barrios o caseríos del mismo municipio y vías de comunicación con los municipios colindantes. Son estos los caminos que recorrieron desde antaño los pobladores de esta isla, testigos de su historia; sendas, verdaderos vectores de comunicación previos al vehículo a motor, por los que, a pie o ayudados por bestias de carga, se transportaban alimentos y otros recursos, así como el correo oral y escrito entre las poblaciones casi siempre aisladas por un relieve abrupto.</p>

<p>Gracias a una nueva sensibilidad social, preocupada por recuperar costumbres y tradiciones -valores que identifican a un pueblo-, y a una nueva forma de entender y relacionarse con el entorno, las instituciones públicas han ido recuperando y rehabilitando parte de esta red de senderos, a tramos escondida bajo la vegetación, para incorporarlos como recurso en el desarrollo endógeno de los municipios de la comarca.</p>

<h2>Objetivos de la guía de senderos:</h2>

<ul>
    <li>Proporcionar una herramienta útil que permita a los visitantes y, en particular, a los senderistas que se acercan a la comarca, conocer los valores patrimoniales naturales y culturales presentes en la misma.</li>
    <li>Ofrecer un recurso pedagógico en los procesos formativos de la ciudadanía, realzando los valores de la educación ambiental al acercar a los ciudadanos al conocimiento del medio.</li>
    <li>Difundir el conocimiento plural e integrador que aportan los caminos desde los aspectos histórico-cultural, ecológico-paisajístico, turístico-recreativo, socio-antropológico, etc.</li>
    <li>Presentar un completo muestrario de paisajes variados, utilizando los caminos como corredores que conectan e intercomunican importantes espacios naturales del territorio insular.</li>
</ul>

